    private void remplirTheme() {
       List<Theme> listTheme = new ArrayList<Theme>();
       
       DefaultTableModel tblModel = (DefaultTableModel) TbTheme.getModel();
              tblModel.setColumnIdentifiers(new Object[]{"Theme_id","Intitule _Theme","Description_Theme"});
        try {
            int rowCount = tblModel.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                tblModel.removeRow(i);
            }
            
            listTheme = ThemeServices.GetAll();
            
            for(int i = 0; i< listTheme.size(); i++){
                Theme obj = listTheme.get(i);
                
                tblModel.addRow(new Object[]{obj.getId(), obj.getIntituleTheme(), obj.getDescriptionTheme()});
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Erreur !!!!");
        }
    }
  
