    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        //Button Supprimer
        
        int theme_id = Integer.parseInt(jTextField1.getText());

        try {
                
            Boolean Deleted = ThemeServices.SupprimerTheme(theme_id);
            
            if(Deleted == true){
                JOptionPane.showMessageDialog(null, "Theme a été supprimé avec succès");
                remplirTheme();
                Effacer();
            }else{
                JOptionPane.showMessageDialog(null, "Erreur de supprimé");
            }

        } catch(Exception ex){
         JOptionPane.showMessageDialog(this,"Erreur !!!!");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

