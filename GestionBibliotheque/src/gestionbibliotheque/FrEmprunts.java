    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
        //Button Rechercher
              
        Connection con=ConnectionUtil.getConnection();
        if(con!=null){
            try {
                PreparedStatement pstmt=con.prepareStatement("select  livre_id, adherent_id, date_emprunt,date_retour,rendu,date_rendu  from Emprunts where emprunt_id=?");
                pstmt.setInt(1, Integer.parseInt(jTextField1.getText()));
               
                ResultSet rs=pstmt.executeQuery();
                if(rs.next()){
                    
                     int intLiId = rs.getInt(1);
                    String strLiId = String.valueOf(intLiId);
                    
                    int intAhId = rs.getInt(2);
                    String strAhId = String.valueOf(intAhId);
                    
                    
                    
                    jTextField2.setText(strLiId);
                    jTextField3.setText(strAhId);
                    //jTextField4.setText(strDateEmpr);
                    //jTextField6.setText(rs.getRendu());
                    
                    
                }
                rs.close();
                pstmt.close();
                con.close();
                
            } catch(Exception ex){
             JOptionPane.showMessageDialog(this,"Erreur !!!!");
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

