    private void remplirEmprunts() {
       List<Emprunts> listEmprunts = new ArrayList<Emprunts>();
       
       DefaultTableModel tblModel = (DefaultTableModel) TbEmprunts.getModel();
              tblModel.setColumnIdentifiers(new Object[]{"Emprunt_id","Livre_id","Adherent_id","Date_Emprunt","Date_retour","Rendu","Date_rendu"});
        try {
            int rowCount = tblModel.getRowCount();
            for (int i = rowCount - 1; i >= 0; i--) {
                tblModel.removeRow(i);
            }
            
            listEmprunts = EmpruntsServices.GetAll();
            
            for(int i = 0; i< listEmprunts.size(); i++){
                Emprunts obj = listEmprunts.get(i);
                String strRendu = (obj.getRendu() == true) ? "Oui" : "Non";
                
                String strDateEmp = "";
                
                if(obj.getDatEmprunt() != null && obj.getDatEmprunt() != ""){
                    SimpleDateFormat sourceDate = new SimpleDateFormat("yyyy-MM-dd");
                    SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                    strDateEmp = dateFormat.format(sourceDate.parse(obj.getDatEmprunt()));
                }
                String strDateRet= "";
                if(obj.getDateRetour() != null && obj.getDateRetour() != ""){
                    SimpleDateFormat sourceDate = new SimpleDateFormat("yyyy-MM-dd");
                    SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
                    strDateRet = dateFormat.format(sourceDate.parse(obj.getDateRetour()));
                }
                
                tblModel.addRow(new Object[]{obj.getEmpruntId(), obj.getLivreId(), obj.getAdherentId(),strDateEmp,strDateRet, strRendu ,obj.getDateRendu()});
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Erreur !!!!");
        }
    }

